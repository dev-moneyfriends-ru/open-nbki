<?php

namespace mfteam\nbch\models;

/**
 * Сведения о залоге
 */
class CollateralRUTDF extends BaseItem
{
    /**
     * Код предмета залога @see CollateralRUTDF::collateralCodeList()
     * @var string $collateralCode
     */
    public $collateralCode = '';
    
    /**
     * Идентификационный код предмета залога
     * Указывается:
     * кадастровый номер – для имеющей такой номер недвижимости;
     * идентификационный номер транспортного средства (VIN) – для имеющего такой номер транспортного средства;
     * код по Общероссийскому классификатору основных фондов (далее – ОКОФ) или заводской номер – для промышленных машин и оборудования;
     * штриховой код – для имеющего такой код товара;
     * уникальный идентификатор финансового инструмента в торговой системе (тикер) – для имеющих такой идентификатор ценной бумаги или иного финансового инструмента.
     * Для предметов залога, не поименованных выше, указывается предусмотренное договором обозначение.
     * @var string $collateralId
     */
    public $collateralId = '';
    
    /**
     * Дата заключения договора залога
     * @var string $collateralAgreementDt
     */
    public $collateralAgreementDt = '';
    
    /**
     * Стоимость предмета залога.
     * Заполняется согласно заключению об оценке стоимости предмета залога.  если оценка стоимости предмета залога не проводилась,
     * указывается цена приобретения предмета залога либо его стоимость по договору залога (по выбору источника).
     * @var float $collateralValue
     */
    public $collateralValue;
    
    /**
     * Валюта стоимости предмета залога
     * @var string $currencyCode
     */
    public $currencyCode = 'RUB';
    
    /**
     * Дата проведения оценки предмета залога.
     * Заполняется, если стоимость предмета залога оценивалась.
     * @var string $collateralDate
     */
    public $collateralDate = '';
    
    /**
     * Признак иного обременения предмета залога
     * Не заполняется, начиная с версии 5.0 настоящего формата (на основании Указания Банка России 6300-У).
     * В предыдущих версиях в этом показателе указывался код «1» – имеется иное обременение предмета залога,
     * либо код «0» – обстоятельство кода «1» отсутствовало.
     * @var int|null $collateralEncum
     * @deprecated
     */
    public $collateralEncum = null;
    
    /**
     * Дата прекращения залога согласно договору
     * @var string $collateralExpirationDate
     */
    public $collateralExpirationDate = '';
    
    /**
     * Дата фактического прекращения залога
     * @var string $collateralFactExpirationDate
     */
    public $collateralFactExpirationDate = '';
    
    /**
     * Код причины прекращения залога
     * 1    Обеспечивающее обязательство прекращено в связи с надлежащим исполнением основного обязательства
     * 2    Обеспечивающее обязательство прекращено в счет погашения требований кредитора по основному обязательству
     * 3    Залог прекращен в связи с гибелью или утратой заложенного имущества
     * 99    Обеспечивающее обязательство прекращено на ином основании
     * @var string $collateralEndReason
     */
    public $collateralEndReason = '';
    
    /**
     * Вид стоимости предмета залога
     * 1    Рыночная
     * 2    Кадастровая
     * 3    Ликвидационная
     * 4    Инвестиционная
     * 99    Иная
     * @var string $collateralValueType
     */
    public $collateralValueType = '';
    
    /**
     * Сумма обязательств по всем договорам, исполнение которых обеспечено предметом залога
     * @var float $collateralcommAmt
     */
    public $collateralcommAmt;
    
    /**
     * Количество договоров, исполнение обязательств по которым обеспечено предметом залога.
     * Количество договоров, обязательства из которых обеспечиваются одним и тем же предметом залога.
     * @var int|null $collateralcontCnt
     */
    public $collateralcontCnt = null;

    /**
     * Код места нахождения залога по ОКАТО.
     * Код субъекта Российской Федерации, на территории которого находится предмет залога, согласно ОКАТО. При отсутствии в ОКАТО кода населенного пункта по показателю «Код места нахождения залога по ОКАТО» указывается «99 999 999 999»
     * @var string
     */
    public $location = '';

    /**
     * Актуальная стоимость предмета залога.
     * Последняя рассчитанная стоимость предмета залога.
     * @var float|null $actualCost
     */
    public $actualCost = null;

    /**
     * Дата расчета актуальной стоимости предмета залога
     * Последняя дата, по состоянию на которую рассчитана стоимость предмета залога.
     * @var string $actualCost
     */
    public $calcDate = '';

