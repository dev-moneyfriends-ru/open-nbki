    /**
     * @inheritDoc
     */
    public function validate(): bool
    {
        $trade = $this->template->sendData->getAccountReplyRUTDF()->getTrade();
        if ($trade === null) {
            $this->errors[] = 'Отсутствует сделка';
        }
        return $this->getIsEmptyErrors();
    }
    
    /**
     * @inheritDoc
     */
    public function getSegmentName(): string
    {
        return "C18_TRADE";
    }
    
    /**
     * @inheritDoc
     */
    public function getFields(): array
    {
        $trade = $this->template->sendData->getAccountReplyRUTDF()->getTrade();
        if ($trade === null) {
            throw new \LogicException();
        }
        return [
            0 => $this->getSegmentName(),
            1 => $trade->ownerIndic,
            2 => $this->formatDate($trade->openedDt),
            3 => $trade->tradeTypeCode,
            4 => $trade->loanKindCode,
            5 => $trade->acctType,
            6 => $trade->isConsumerLoan,
            7 => $trade->hasCard,
            8 => $trade->isNovation,
            9 => $trade->isMoneySource,
            10 => $trade->isMoneyBorrower,
            11 => $this->formatDate($trade->closeDt),
            12 => $trade->lendertypeCode,
            13 => $trade->obtainpartCred,
            14 => $trade->creditLine,
            15 => $trade->creditLineCode,
            16 => $trade->interestrateFloat,
            17 => $trade->transpartCred,
            18 => $trade->transpartCredUuid? $trade->transpartCredUuid . "-" . $trade->getUuidControlSum($trade->transpartCredUuid): self::EMPTY_VALUE,
            19 => $this->formatDate($trade->commitDate),
        ];
    }
    
